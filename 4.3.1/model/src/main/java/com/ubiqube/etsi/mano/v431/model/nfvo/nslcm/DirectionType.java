package com.ubiqube.etsi.mano.v431.model.nfvo.nslcm;

import java.util.Objects;
import io.swagger.v3.oas.annotations.media.Schema;
import io.swagger.v3.oas.annotations.media.Schema.RequiredMode;
import com.fasterxml.jackson.annotation.JsonValue;
import org.springframework.validation.annotation.Validated;
import jakarta.validation.Valid;
import jakarta.validation.constraints.*;

import com.fasterxml.jackson.annotation.JsonCreator;

/**
 * The enumeration DirectionType represents the direction of the data flow that are requested to be mirrored. It shall comply with the provisions defined in table 6.5.4.12-1.
 */
public enum DirectionType {
  IN("IN"),
    OUT("OUT"),
    BOTH("BOTH");

  private String value;

  DirectionType(String value) {
    this.value = value;
  }

  @Override
  @JsonValue
  public String toString() {
    return String.valueOf(value);
  }

  @JsonCreator
  public static DirectionType fromValue(String text) {
    for (DirectionType b : DirectionType.values()) {
      if (String.valueOf(b.value).equals(text)) {
        return b;
      }
    }
    return null;
  }
}
